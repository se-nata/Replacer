package replacer;

public class Zamena {
	private String oldstr;
	private String newstr;

	public Zamena(String oldstr, String newstr) {
		super();
		this.oldstr = oldstr;
		this.newstr = newstr;
	}

	public String getOldstr() {
		return oldstr;
	}

	public String getNewstr() {
		return newstr;
	}
}
